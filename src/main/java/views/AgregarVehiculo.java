    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Verificamos que esten todos los datos ingresados
        if (!tfpatente.getText().equals("") && !tfanio.getText().equals("") && !tfmodelo.getText().equals("") && !tfcapacidad.getText().equals("")) {
            if (ltipo.getSelectedItem().equals("COMBUSTIBLE") && !tfkmlitro.getText().equals("") && !tflextra.getText().equals("")) {
                armarVehiculo();
            } else if(ltipo.getSelectedItem().equals("ELECTRICO") && !tfkwhbase.getText().equals("")){
                armarVehiculo();
            } else {
                // Algun dato especifico correspondientes a los tipos no se ingreso
                JOptionPane.showMessageDialog(this,"Datos Incompletos");
            }
        } else {
            // Alguno de los datos comunes de los vehiculos no se ingreso
            JOptionPane.showMessageDialog(this,"Datos Incompletos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ltipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ltipoItemStateChanged
        // Al seleccionar un item nos aseguramos que no se puedan ingresar datos que no correspondan
        if (ltipo.getSelectedItem().equals("ELECTRICO")) {
            tfkwhbase.setEnabled(true);
            tfkmlitro.setText("");
            tfkmlitro.setEnabled(false);
            tflextra.setText("");
            tflextra.setEnabled(false);
        }
        else {
            tfkwhbase.setText("");
            tfkwhbase.setEnabled(false);
            tfkmlitro.setEnabled(true);
            tflextra.setEnabled(true);
        }
    }//GEN-LAST:event_ltipoItemStateChanged

