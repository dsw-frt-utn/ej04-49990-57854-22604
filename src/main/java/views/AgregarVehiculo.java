    public AgregarVehiculo() {
        initComponents();
        // Obtenemos los datos para cargar las listas
        for (Marca m : Persistencia.getMarcas()) {
            lmarca.addItem(m.getNombre()); // muestra el nombre de la marca
        }
        for (Sucursal s : Persistencia.getSucursales()) {
            lsucursal.addItem(s.getCodigo()); // muestra codigo de la sucursal
        }
        ltipo.addItem(VehiculoTipo.ELECTRICO.toString());
        ltipo.addItem(VehiculoTipo.COMBUSTIBLE.toString());
        // Limpiamos los Textfield
        limpiarDatos();
    }
    
    public final void limpiarDatos(){
        tfpatente.setText("");
        tfanio.setText("");
        tfmodelo.setText("");
        tfcapacidad.setText("");
        tfkwhbase.setText("");
        tfkmlitro.setText("");
        tflextra.setText("");
        if (ltipo.getSelectedItem() == "ELECTRICO"){
            tfkmlitro.setEnabled(false);
            tflextra.setEnabled(false);
            tfkwhbase.setEnabled(true);
        }
        else {
            tfkmlitro.setEnabled(true);
            tflextra.setEnabled(true);
            tfkwhbase.setEnabled(false);
        }
    }
    
    public final void armarVehiculo(){
        // Recolectamos los datos ingresados
        String patente = tfpatente.getText();
        String modelo = tfmodelo.getText();
        Double capacidad = Double.valueOf(tfcapacidad.getText());
        int anio = Integer.parseInt(tfanio.getText());
        Sucursal sucursal = null;
        for (Sucursal s : Persistencia.getSucursales()){
            if (s.getCodigo() == lsucursal.getSelectedItem()){
                sucursal = s;
                break;
            }
        }
        Marca marca = null;
        for (Marca m : Persistencia.getMarcas()){
            if (m.getNombre() == lmarca.getSelectedItem()){
                marca = m;
                break;
            }
        }
        // Verificamos que tipo de Vehiculo ingreso
        Vehiculo v = null;
        if (ltipo.getSelectedItem() == "ELECTRICO"){
            // Creamos un vehiculo ELECTRICO
            Double kwhbase = Double.valueOf(tfkwhbase.getText());
            v = new VehiculoElectrico(patente,marca,modelo,anio,capacidad,sucursal,kwhbase);
        }
        else {
            // Creamos un vehiculo COMBUSTIBLE
            Double kmlitro = Double.valueOf(tfkmlitro.getText());
            Double lextra= Double.valueOf(tflextra.getText());
            v = new VehiculoCombustible(patente,marca,modelo,anio,capacidad,sucursal,kmlitro,lextra);
        }
        // Enviamos los datos a control para guardar el vehiculo
        String respuesta = Controlador.agregarNuevoVehiculo(v);
        // Verificamos la respuesta
        if (respuesta.equals("Ok")){
            // Si se puede ingresar limpiamos los datos y mostramos el OK
            limpiarDatos();
            JOptionPane.showMessageDialog(this, "Ingresado con Exito");
        }
        else {
            // Si no s epuede lo informamos y dejamos los datos para correccion
            JOptionPane.showMessageDialog(this, "La patente ya existe, ingrese otra patente");
        }
    }

